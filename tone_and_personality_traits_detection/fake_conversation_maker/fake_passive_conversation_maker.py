def _format_conversation(
    messages: List[tuple],
    conversation_id: str,
    base_time: datetime,
    participant_a: str = None,
    participant_b: str = None,
) -> List[Dict]:
    records = []
    
    if participant_a is None or participant_b is None:
        participants = set(msg[0] for msg in messages)
        participants = list(participants)
        if len(participants) >= 2:
            participant_a = participants[0]
            participant_b = participants[1]
        elif len(participants) == 1:
            participant_a = participants[0]
            participant_b = participants[0]
    
    for sender, text, minutes_offset in messages:
        timestamp = base_time + timedelta(minutes=minutes_offset)
        to_user = participant_b if sender == participant_a else participant_a
        records.append({
            "user_id": sender,
            "to_user_id": to_user,
            "conversation_id": conversation_id,
            "message_id": str(uuid.uuid4()),
            "message": text,
            "language": "fa",
            "timestamp_iso": timestamp.isoformat(),
            "ts": timestamp,
            "deleted": "false"
        })
    return records


